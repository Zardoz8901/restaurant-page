export default function locationRender() {
    const createDiv = () => {
        const div = document.createElement('div');
        return div;
    };
    const createSpan = () => {
        const span = document.createElement('span');
        return span;
    };
    const createP = () => {
        const p = document.createElement('p');
        return p;
    };
    const createAnchor = () => {
        const a = document.createElement('a');
        return a;
    };
}
